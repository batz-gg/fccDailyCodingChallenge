/**
 * Хоолны үнэ болон тусгай зөвлөгөөний хувийг ашиглан 15%, 20%, болон тусгай хувийн зөвлөгөөний дүнг тооцоолно.
 * Үр дүнг "$N.NN" форматаар буцаана.
 * * @param {string} mealPrice - Хоолны үнэ ("$N.NN" форматтай).
 * @param {string} customPercent - Тусгай зөвлөгөөний хувь ("X%" форматтай).
 * @returns {string[]} - Гурван зөвлөгөөний дүнгийн массив (["$1.50", "$2.00", "$2.50" гэх мэт]).
 */
function calculateTips(mealPrice, customPercent) {
    // 1. Үнийн тэмдэгтээс ($) салгаж, тоон утгад хөрвүүлэх.
    const price = parseFloat(mealPrice.replace('$', ''));

    // 2. Тусгай хувийн тэмдэгтээс (%) салгаж, хувийн утгыг олох (жишээ нь, "25%" -> 0.25).
    const customRate = parseFloat(customPercent.replace('%', '')) / 100;

    // 3. Зөвлөгөөний хувиудыг тодорхойлох.
    const tipRates = [0.15, 0.20, customRate];

    // 4. Зөвлөгөөний дүнг тооцоолж, дугуйлах функц.
    const formatTip = (rate) => {
        // Дүнг тооцоолох (100-аар үржүүлж, дугуйлаад, 100-д хуваах нь хоёр оронтой дугуйлах найдвартай арга).
        const tipAmount = Math.round(price * rate * 100) / 100;
        
        // "$N.NN" форматаар хөрвүүлэх (жишээ нь, 1.5 -> "1.50").
        return `$${tipAmount.toFixed(2)}`;
    };

    // 5. Гурван зөвлөгөөний дүнг тооцоолж, массивт буцаах.
    return tipRates.map(formatTip);
}

// Жишээ 1:
// calculateTips("$10.00", "25%")
// Үнэ: 10.00. Хувь: 0.25
// 15%: 10.00 * 0.15 = 1.50 -> "$1.50"
// 20%: 10.00 * 0.20 = 2.00 -> "$2.00"
// 25%: 10.00 * 0.25 = 2.50 -> "$2.50"
// Гаралт: ["$1.50", "$2.00", "$2.50"]