/**
 * Өгөгдсөн ачааны массыг хөөргөхөд шаардлагатай нийт түлшийг тооцоолно.
 * @param {number} payload - Ачааны масс (кг).
 * @returns {number} - Шаардлагатай нийт түлшний хэмжээ (нэг оронгоор бөөрөнгийдсөн).
 */
function launchFuel(payload) {
    let currentMass = payload; // Одоо өргөх ёстой масс (Эхлээд зөвхөн ачаа)
    let totalFuel = 0;         // Нийт шаардлагатай түлш
    
    // Давталт: Нэмэлт түлш 1 кг-аас их байх нөхцөлд үргэлжилнэ.
    while (true) {
        // 1. Одоо байгаа масст шаардлагатай нэмэлт түлшийг тооцох.
        const additionalFuel = currentMass / 5;

        // 2. Хэрэв нэмэлт түлш 1 кг-аас бага бол давталтыг зогсооно.
        if (additionalFuel < 1) {
            // Энэ түлш нь нийт түлшний хэмжээнд нэмэгдээд зогсоно.
            totalFuel += additionalFuel;
            break; 
        }

        // 3. Нийт түлшний хэмжээг шинэчилнэ.
        totalFuel += additionalFuel;

        // 4. Өргөх ёстой дараагийн масс нь нэмэлт түлшний хэмжээ болно.
        // Учир нь бид өмнөх шатанд тооцоолсон түлшийг л нэмж өргөх хэрэгтэй.
        currentMass = additionalFuel;
    }

    // Үр дүнг нэг аравтын бутархай оронгоор бөөрөнгийдөж, number хэлбэрт шилжүүлэн буцаана.
    return +totalFuel.toFixed(1);
}